    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        BeanVenda v;
        v=dao.readForId(Integer.parseInt(txtID.getText()));
        String data = jtVenda.getValueAt(jtVenda.getSelectedRow(), 3).toString();
        v.setData(dao.dataJava(data));
        int reply = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente remover esta venda? \nProduto: "+v.getNomeProduto()+"\nQuantidade: "+String.valueOf(v.getQtd())+"\nValor:"+String.valueOf(v.getValor())+"\nData: "+v.getData().toString());
        if(reply==JOptionPane.YES_OPTION){
            dao.remove(Integer.parseInt(txtID.getText()));
            dao.readVendaJIF(jtVenda);
            limpaCampos();
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        BeanVenda v = new BeanVenda();
        DaoVenda dao = new DaoVenda();
        Date dataA;
        
        if(cbHoje.isSelected()){
            dataA = new Date(System.currentTimeMillis());
            v.setDataSql(dataA);
            if (isValidNumbers(Integer.parseInt(txtQTD.getText()), Double.parseDouble(txtValor.getText()))) {
                confirmaUpdate(v);
            }
        } else {
            if (isValidDate(jfData.getText(), "dd-MM-yyyy") && isValidNumbers(Integer.parseInt(txtQTD.getText()), Double.parseDouble(txtValor.getText()))) {
                dataA = Date.valueOf(dataSql(jfData.getText()));
                v.setDataSql(dataA);
                confirmaUpdate(v);
            }
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

