    public boolean isValidDate(String date, String format) {         
	SimpleDateFormat dateFormat = new SimpleDateFormat(format);     
	dateFormat.setLenient(false);                                   
	try {                                                           
		dateFormat.parse(date);                                     
		return true;                                                
	} catch (ParseException e) {  
                JOptionPane.showMessageDialog(null, "Data invalida.");
		return false;                                               
        }
    }

    public boolean isValidNumbers(int qtd, double valor) {
        if (qtd >= 0 && valor >= 0) {
            return true;
        }
        else{
            JOptionPane.showMessageDialog(null, "Não informe numeros negativos nos campos 'Quantidade' e 'Valor'.");
            return false;
        }
    }

    public String dataJava(String data) {
        String ano = data.substring(0, 4);
        String mes = data.substring(4, 8);
        String dia = data.substring(8, 10);
        data = dia + mes + ano;
        return data;
    }
    public String dataSql(String data){
        String ano = data.substring(6, 10);
        String mes = data.substring(2, 6);
        String dia = data.substring(0, 2);
        data = ano+mes+dia;
        return data;
    }
    public void confirmaUpdate(BeanVenda v){
        v.setQtd(Integer.parseInt(txtQTD.getText()));
        v.setValor(Double.parseDouble(txtValor.getText()));
        v.setId(Integer.parseInt(txtID.getText()));
        v.setNomeProduto(jtVenda.getValueAt(jtVenda.getSelectedRow(), 1).toString());
        int reply = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente atualizar esta venda com estas informações?\n ID Venda: " + v.getId() + " \nProduto: " + v.getNomeProduto() + "\nQuantidade: " + String.valueOf(v.getQtd()) + "\nValor: " + String.valueOf(v.getValor()) + "\nData: " + v.getDataSql().toString());
            if (reply == JOptionPane.YES_OPTION) {
                dao.update(v);
                dao.readVendaJIF(jtVenda);;
                limpaCampos();
            }
    }
